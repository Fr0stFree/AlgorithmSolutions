-- solution
SELECT
    MIN(month_pass) AS months
FROM
    (SELECT
        *,
        ROW_NUMBER() OVER(ORDER BY year, month) AS month_pass,
        SUM(income - outcome) OVER(ORDER BY year, month) AS ror
    FROM
        revenues) AS temp
WHERE ror > 0;