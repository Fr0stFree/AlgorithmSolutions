-- В прошлой задаче мы рассчитали размер банковского кредита, который необходим для открытия новой точки. Теперь надо
-- вычислить срок окупаемости инвестиций. Инвестиции окупаются в тот момент, когда суммарные доходы бизнеса превышают
-- суммарные расходы. Напишите SQL-запрос, который выведет один столбец (months), содержащий одну единственную строку
-- — количество месяцев, через которое бизнес окупит все вложенные в него средства.

-- init
DROP TABLE IF EXISTS revenues;
CREATE TABLE revenues (
    year INTEGER NULL,
    month INTEGER NULL,
    income INTEGER NULL,
    outcome INTEGER NULL
);
INSERT INTO revenues (year, month, income, outcome)
VALUES
