import math


def find_smallest_node_lengths(volume: int) -> list[int]:
    smallest_area = math.inf
    best_cube = [0, 0, 0]

    for a in range(1, int(math.isqrt(volume)) + 1):
        if volume % a != 0:
            continue

        for b in range(a, int(math.isqrt(volume // a)) + 1):
            if (volume // a) % b != 0:
                continue

            c = volume // (a * b)
            area = 2 * (a * b + b * c + c * a)

            if area < smallest_area:
                smallest_area = area
                best_cube = [a, b, c]

    return best_cube


